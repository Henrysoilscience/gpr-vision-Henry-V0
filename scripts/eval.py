def binarize(prediction: np.ndarray, threshold: float) -> np.ndarray:
    return (prediction >= threshold).astype(np.uint8)

def load_mask(path: pathlib.Path) -> np.ndarray:
    return (np.load(path) > 0.5).astype(np.uint8)


def load_pred_mask(scene_dir: pathlib.Path, threshold: float) -> np.ndarray:
    mask_path = scene_dir / "mask.npy"
    probs_path = scene_dir / "probs.npy"
    logits_path = scene_dir / "logits.npy"
    if mask_path.exists():
        return (np.load(mask_path) > 0.5).astype(np.uint8)
    if probs_path.exists():
        return (np.load(probs_path) >= threshold).astype(np.uint8)
    if logits_path.exists():
        logits = np.load(logits_path)
        probs = 1.0 / (1.0 + np.exp(-logits))
        return (probs >= threshold).astype(np.uint8)
    raise FileNotFoundError(f"No mask/probs/logits found in {scene_dir}")


def connected_components(mask: np.ndarray) -> List[Component]:
    h, w = mask.shape
    visited = np.zeros_like(mask, dtype=bool)
    components: List[Component] = []

    for y in range(h):
        for x in range(w):
            if mask[y, x] == 0 or visited[y, x]:
                continue
            stack = [(y, x)]
            visited[y, x] = True
            pixels: set[Tuple[int, int]] = set()
            min_y, min_x, max_y, max_x = y, x, y, x
            while stack:
                cy, cx = stack.pop()
                pixels.add((cy, cx))
                min_y = min(min_y, cy)
                min_x = min(min_x, cx)
                max_y = max(max_y, cy)
                max_x = max(max_x, cx)
                for ny, nx in ((cy - 1, cx), (cy + 1, cx), (cy, cx - 1), (cy, cx + 1)):
                    if 0 <= ny < h and 0 <= nx < w and not visited[ny, nx] and mask[ny, nx] == 1:
                        visited[ny, nx] = True
                        stack.append((ny, nx))
            components.append(Component(bbox=(min_x, min_y, max_x, max_y), pixels=pixels))

    return components


def mask_confusion(ground: np.ndarray, pred: np.ndarray) -> Dict[str, float]:
    tp = float(np.logical_and(ground == 1, pred == 1).sum())
    fp = float(np.logical_and(ground == 0, pred == 1).sum())
    fn = float(np.logical_and(ground == 1, pred == 0).sum())
    return {"tp": tp, "fp": fp, "fn": fn}


def safe_div(num: float, den: float) -> float:
    return num / den if den > 0 else 0.0


def component_iou(a: Component, b: Component) -> float:
    inter = len(a.pixels.intersection(b.pixels))
    union = len(a.pixels.union(b.pixels))
    return safe_div(float(inter), float(union))


def match_components(
    gt_components: List[Component],
    pred_components: List[Component],
    iou_threshold: float,
) -> Tuple[int, int, int]:
    used_pred: set[int] = set()
    tp = 0
    for gt in gt_components:
        best_idx = -1
        best_iou = 0.0
        for idx, pred in enumerate(pred_components):
            if idx in used_pred:
                continue
            iou = component_iou(gt, pred)
            if iou > best_iou:
                best_iou = iou
                best_idx = idx
        if best_idx >= 0 and best_iou >= iou_threshold:
            tp += 1
            used_pred.add(best_idx)

    fp = len(pred_components) - len(used_pred)
    fn = len(gt_components) - tp
    return tp, fp, fn


def average_precision(
    gt_components: List[Component],
    pred_components: List[Component],
    iou_threshold: float,
) -> float:
    tp, fp, fn = match_components(gt_components, pred_components, iou_threshold)
    precision = safe_div(float(tp), float(tp + fp))
    recall = safe_div(float(tp), float(tp + fn))
    return precision * recall


def evaluate_scene(gt_mask: np.ndarray, pred_mask: np.ndarray) -> Dict[str, float]:
    confusion = mask_confusion(gt_mask, pred_mask)
    precision = safe_div(confusion["tp"], confusion["tp"] + confusion["fp"])
    recall = safe_div(confusion["tp"], confusion["tp"] + confusion["fn"])
    f1 = safe_div(2 * precision * recall, precision + recall)
    miou = safe_div(confusion["tp"], confusion["tp"] + confusion["fp"] + confusion["fn"])

