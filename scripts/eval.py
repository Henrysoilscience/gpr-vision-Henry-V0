"""Evaluate model predictions against ground truth with real matching metrics."""

from __future__ import annotations

import argparse
import json
import pathlib
from dataclasses import dataclass
from typing import Dict, List, Tuple

import numpy as np


@dataclass
class Component:
    """Connected component represented by bounding box and pixel set."""

    bbox: Tuple[int, int, int, int]
    pixels: set[Tuple[int, int]]


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Evaluate segmentation outputs with object matching.")
    parser.add_argument("ground_truth", type=pathlib.Path, help="Root directory with scene_*/mask.npy labels.")
    parser.add_argument("predictions", type=pathlib.Path, help="Directory generated by scripts/infer.py.")
    parser.add_argument("--threshold", type=float, default=0.5, help="Threshold for probability maps if masks missing.")
    parser.add_argument("--report-json", type=pathlib.Path, default=pathlib.Path("eval_report.json"))
    parser.add_argument("--summary-txt", type=pathlib.Path, default=pathlib.Path("eval_summary.txt"))
    return parser.parse_args()


def load_mask(path: pathlib.Path) -> np.ndarray:
    return (np.load(path) > 0.5).astype(np.uint8)


def load_pred_mask(scene_dir: pathlib.Path, threshold: float) -> np.ndarray:
    mask_path = scene_dir / "mask.npy"
    probs_path = scene_dir / "probs.npy"
    logits_path = scene_dir / "logits.npy"
    if mask_path.exists():
        return (np.load(mask_path) > 0.5).astype(np.uint8)
    if probs_path.exists():
        return (np.load(probs_path) >= threshold).astype(np.uint8)
    if logits_path.exists():
        logits = np.load(logits_path)
        probs = 1.0 / (1.0 + np.exp(-logits))
        return (probs >= threshold).astype(np.uint8)
    raise FileNotFoundError(f"No mask/probs/logits found in {scene_dir}")


def connected_components(mask: np.ndarray) -> List[Component]:
    h, w = mask.shape
    visited = np.zeros_like(mask, dtype=bool)
    components: List[Component] = []

    for y in range(h):
        for x in range(w):
            if mask[y, x] == 0 or visited[y, x]:
                continue
            stack = [(y, x)]
            visited[y, x] = True
            pixels: set[Tuple[int, int]] = set()
            min_y, min_x, max_y, max_x = y, x, y, x
            while stack:
                cy, cx = stack.pop()
                pixels.add((cy, cx))
                min_y = min(min_y, cy)
                min_x = min(min_x, cx)
                max_y = max(max_y, cy)
                max_x = max(max_x, cx)
                for ny, nx in ((cy - 1, cx), (cy + 1, cx), (cy, cx - 1), (cy, cx + 1)):
                    if 0 <= ny < h and 0 <= nx < w and not visited[ny, nx] and mask[ny, nx] == 1:
                        visited[ny, nx] = True
                        stack.append((ny, nx))
            components.append(Component(bbox=(min_x, min_y, max_x, max_y), pixels=pixels))

    return components


def mask_confusion(ground: np.ndarray, pred: np.ndarray) -> Dict[str, float]:
    tp = float(np.logical_and(ground == 1, pred == 1).sum())
    fp = float(np.logical_and(ground == 0, pred == 1).sum())
    fn = float(np.logical_and(ground == 1, pred == 0).sum())
    return {"tp": tp, "fp": fp, "fn": fn}


def safe_div(num: float, den: float) -> float:
    return num / den if den > 0 else 0.0


def component_iou(a: Component, b: Component) -> float:
    inter = len(a.pixels.intersection(b.pixels))
    union = len(a.pixels.union(b.pixels))
    return safe_div(float(inter), float(union))


def match_components(
    gt_components: List[Component],
    pred_components: List[Component],
    iou_threshold: float,
) -> Tuple[int, int, int]:
    used_pred: set[int] = set()
    tp = 0
    for gt in gt_components:
        best_idx = -1
        best_iou = 0.0
        for idx, pred in enumerate(pred_components):
            if idx in used_pred:
                continue
            iou = component_iou(gt, pred)
            if iou > best_iou:
                best_iou = iou
                best_idx = idx
        if best_idx >= 0 and best_iou >= iou_threshold:
            tp += 1
            used_pred.add(best_idx)

    fp = len(pred_components) - len(used_pred)
    fn = len(gt_components) - tp
    return tp, fp, fn


def average_precision(
    gt_components: List[Component],
    pred_components: List[Component],
    iou_threshold: float,
) -> float:
    tp, fp, fn = match_components(gt_components, pred_components, iou_threshold)
    precision = safe_div(float(tp), float(tp + fp))
    recall = safe_div(float(tp), float(tp + fn))
    return precision * recall


def evaluate_scene(gt_mask: np.ndarray, pred_mask: np.ndarray) -> Dict[str, float]:
    confusion = mask_confusion(gt_mask, pred_mask)
    precision = safe_div(confusion["tp"], confusion["tp"] + confusion["fp"])
    recall = safe_div(confusion["tp"], confusion["tp"] + confusion["fn"])
    f1 = safe_div(2 * precision * recall, precision + recall)
    miou = safe_div(confusion["tp"], confusion["tp"] + confusion["fp"] + confusion["fn"])

    gt_comp = connected_components(gt_mask)
    pred_comp = connected_components(pred_mask)
    ap_50 = average_precision(gt_comp, pred_comp, 0.5)
    ap_75 = average_precision(gt_comp, pred_comp, 0.75)
    map_score = (ap_50 + ap_75) / 2.0

    return {
        "tp": confusion["tp"],
        "fp": confusion["fp"],
        "fn": confusion["fn"],
        "precision": precision,
        "recall": recall,
        "f1": f1,
        "miou": miou,
        "ap50": ap_50,
        "ap75": ap_75,
        "map": map_score,
        "gt_instances": len(gt_comp),
        "pred_instances": len(pred_comp),
    }


def collect_pairs(ground_truth: pathlib.Path, predictions: pathlib.Path) -> List[Tuple[pathlib.Path, pathlib.Path]]:
    pairs: List[Tuple[pathlib.Path, pathlib.Path]] = []
    for gt_path in sorted(ground_truth.glob("scene_*/mask.npy")):
        scene_name = gt_path.parent.name
        pred_scene_dir = predictions / scene_name
        if pred_scene_dir.exists():
            pairs.append((gt_path, pred_scene_dir))
    return pairs


def summarize(rows: List[Dict[str, float]]) -> Dict[str, float]:
    if not rows:
        return {
            "num_scenes": 0,
            "precision": 0.0,
            "recall": 0.0,
            "f1": 0.0,
            "miou": 0.0,
            "map": 0.0,
        }
    keys = ["precision", "recall", "f1", "miou", "map", "ap50", "ap75"]
    out = {key: float(np.mean([row[key] for row in rows])) for key in keys}
    out["num_scenes"] = len(rows)
    return out


def main() -> None:
    args = parse_args()
    pairs = collect_pairs(args.ground_truth, args.predictions)
    per_scene: List[Dict[str, float | str]] = []

    for gt_path, pred_dir in pairs:
        gt_mask = load_mask(gt_path)
        pred_mask = load_pred_mask(pred_dir, args.threshold)
        metrics = evaluate_scene(gt_mask, pred_mask)
        metrics["scene"] = gt_path.parent.name
        per_scene.append(metrics)

    summary = summarize([item for item in per_scene if isinstance(item, dict)])
    report = {
        "summary": summary,
        "per_scene": per_scene,
        "threshold": args.threshold,
    }

    args.report_json.parent.mkdir(parents=True, exist_ok=True)
    args.report_json.write_text(json.dumps(report, indent=2))

    text_lines = [
        "GPR Evaluation Summary",
        f"Scenes evaluated: {summary['num_scenes']}",
        f"mAP: {summary['map']:.4f} (AP50={summary.get('ap50', 0.0):.4f}, AP75={summary.get('ap75', 0.0):.4f})",
        f"mIoU: {summary['miou']:.4f}",
        f"Precision: {summary['precision']:.4f}",
        f"Recall: {summary['recall']:.4f}",
        f"F1: {summary['f1']:.4f}",
    ]
    args.summary_txt.parent.mkdir(parents=True, exist_ok=True)
    args.summary_txt.write_text("\n".join(text_lines) + "\n")

    print(json.dumps(summary, indent=2))


if __name__ == "__main__":
    main()
